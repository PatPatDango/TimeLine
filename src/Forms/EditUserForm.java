    /**
     * Saves the edited user information within the database 
     * @param evt 
     */
    private void button_saveChangesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveChangesActionPerformed
        // overwrite persons information
        DatabaseHandler dbHandler = new DatabaseHandler();
        ArrayList<Appointment> toBeDeleted = new ArrayList<>();
        User edited = this.user;
        //Fetch new information from the textfields
        String new_firstname = txt_firstname.getText();
        String new_lastname = txt_lastname.getText();
        String new_email = txt_email.getText();
        boolean new_role = Admin.isSelected();
        //Edited User
        edited.setEmail(new_email);
        edited.setFirstname(new_firstname);
        edited.setLastname(new_lastname);
        edited.setRole(new_role);

        //Find all Events the Admin wanted the User to be removed from.
        for (int row = 0; row < EventTable.getRowCount(); row++) {
            //EventTable at (x,2) can only be boolean so it's safe to cast
            if ((boolean) EventTable.getValueAt(row, 2) == false) {
                toBeDeleted.add(events.get(row));
            }
        }

        //Either delete an Event or delete the User from the list of Participants
        toBeDeleted.forEach((Appointment Appointment) -> {
            if (user.getUsername().equals(Appointment.getHost())) {
                dbHandler.deleteEvent(Appointment);
            }  else {
                dbHandler.deleteUserFromEvent(user, Appointment);
            }
        });

        //Edit User
        dbHandler.EditUser(edited);
        events = dbHandler.getUsersEvents(user);
        setContent();
        MainFrame.getObject().updateEvents();

    }//GEN-LAST:event_button_saveChangesActionPerformed
