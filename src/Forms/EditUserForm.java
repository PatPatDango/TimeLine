    /**
     * sets the content of the Tables regarding the {@link Appointment} of a User
     */
    private void setContent() {
        eventModel = (DefaultTableModel) EventTable.getModel();
        eventModel.setRowCount(0);
        for (Appointment event : events) {
            eventModel.addRow(
                    new Object[]{
                        event.getName(),
                        event.getHost().getEmail(),
                        true
                    });
        }
        EventTable.setModel(eventModel);
        MainFrame.getObject().updateEvents();
    }
    /**
     * Sets the {@link User} of the form to the {@link User} which is
     * edited at the moment
     * @param user edited user
     */
    private void setUser(User user) {
        this.user = user;
        txt_firstname.setText(user.getFirstname());
        txt_lastname.setText(user.getLastname());
        txt_email.setText(user.getEmail());
        Admin.setSelected(user.getRole());

    }
    /**
     * Sets the {@link ArrayList} of {@link Appointment}'s of a User which is
     * edited
     * @param usersEvents users Events
     */
    private void setUsersEvents(ArrayList<Appointment> usersEvents) {
        events = usersEvents;
    }

