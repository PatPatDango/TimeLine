    /**
     * if you double tap on a chosen dataset of a {@link User}
     * the user editor interface {@link EditUserForm} will open
     * @param evt 
     */
    private void UserTableMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_UserTableMousePressed
        if (evt.getClickCount() == 2) {
            CenterRight.removeAll();
            CenterRight.revalidate();
            CenterRight.repaint();
            int row = UserTable.getSelectedRow();
            EditUserForm JPEditUser = new EditUserForm(user.get(row), AllEvents_user.getUsersEvents(user.get(row)));
            CenterRight.add(JPEditUser);
            JPEditUser.setVisible(true);
        }
    }//GEN-LAST:event_UserTableMousePressed
    
