    /**
     * sets the content of the {@link JTable} respectively to the users in the 
     * database
     */
     private void setContent() {
        userModel = (DefaultTableModel) UserTable.getModel();

        for (Classes.User u : this.user) {
            userModel.addRow(
                    new Object[]{
                        u.getUsername(),
                        u.getFirstname(),
                        u.getLastname(),
                        u.getEmail(),
                        u.getRoleName()
                    });
        }
        UserTable.setModel(userModel);
        //UserTable.setRowSelectionAllowed(true);
        UserTable.setColumnSelectionAllowed(false);
    }
    

